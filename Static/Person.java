package opps.Static;

public class Person {

	int age;
	String name;
	static String breed="Homo Sapeins";
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name=name;
	}
	public int getAge()
	{
		return age;
	}
	public void setAge(int age)
	{
		this.age=age;
	}
	public String getBreed()
	{
		return breed;
	}
}
